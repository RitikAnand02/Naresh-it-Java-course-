//Bitwise Operator
class Test19 {
    public static void main(String[] args) {
        System.out.println(true & true); // true
        System.out.println(false | true); // true
        System.out.println(true ^ true); // true

        System.out.println(6 & 7); // 6
        System.out.println(6 | 7); // 7
        System.out.println(6 ^ 7); // 1
    }
}